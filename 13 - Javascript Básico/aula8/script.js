/*

Operadores lógivos:

igualdade valores == 
igualdade valores e tipos === ( mais usado )
diferença de valores !=
diferença de valores e tipos !==
> maior que
< menor que
>= maior ou igual
<= menor ou igual

*/


// igualdade valores == 
let a = 10
let b = 5

console.log(a == b)


// igualdade valores e tipos ===
let c = 10
let d = 5

console.log(c === d)


// diferença de valores !==
let e = 10
let f = '10'
console.log(e !== f)


// > maior que
console.log(a > b)

// > menor que
console.log(a < b)


// >= maior ou igual
console.log(e >= f)


// <= menor ou igual
console.log(e <= f)
